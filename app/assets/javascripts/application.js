$(document).ready(function() {
    // Setup - add a text input to each footer cell
    $('#company_addresses thead tr').clone(true).appendTo( '#company_addresses thead' );
    $('#company_addresses thead tr:eq(1) th').each( function (i) {
        var title = $(this).text();
        $(this).html( '<input type="text" placeholder="Search '+title+'" />' );

        $( 'input', this ).on( 'keyup change', function () {
            if ( table.column(i).search() !== this.value ) {
                table
                    .column(i)
                    .search( this.value )
                    .draw();
            }
        } );
    } );

    var table = $('#company_addresses').DataTable( {
        orderCellsTop: true,
        fixedHeader: true,
        lengthChange: false,
        pageLength: 50
    } );
} );

